package com.vision.vb;

public class DependencyCategoryVb {
	
	private String country = "";
	private String leBook = "";
	private String categoryId = "";
	private String categoryDesc = "";
	private String dependentCategoryId = "";
	private String dependentCategoryName = "";
	private String groupedCategoryStatus = "";
	
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getLeBook() {
		return leBook;
	}
	public void setLeBook(String leBook) {
		this.leBook = leBook;
	}
	public String getCategoryId() {
		return categoryId;
	}
	public void setCategoryId(String categoryId) {
		this.categoryId = categoryId;
	}
	public String getCategoryDesc() {
		return categoryDesc;
	}
	public void setCategoryDesc(String categoryDesc) {
		this.categoryDesc = categoryDesc;
	}
	public String getDependentCategoryId() {
		return dependentCategoryId;
	}
	public void setDependentCategoryId(String dependentCategoryId) {
		this.dependentCategoryId = dependentCategoryId;
	}
	public String getDependentCategoryName() {
		return dependentCategoryName;
	}
	public void setDependentCategoryName(String dependentCategoryName) {
		this.dependentCategoryName = dependentCategoryName;
	}
	public String getGroupedCategoryStatus() {
		return groupedCategoryStatus;
	}
	public void setGroupedCategoryStatus(String groupedCategoryStatus) {
		this.groupedCategoryStatus = groupedCategoryStatus;
	}
	
}
