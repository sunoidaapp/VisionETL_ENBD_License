package com.vision.vb;

public class SingletonResponseVb {
	private int order;
	private String label = "";
	private int value;
	private String passValues = "";

	public int getOrder() {
		return order;
	}

	public void setOrder(int order) {
		this.order = order;
	}

	public String getLabel() {
		return label;
	}

	public void setLabel(String label) {
		this.label = label;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

	public String getPassValues() {
		return passValues;
	}

	public void setPassValues(String passValues) {
		this.passValues = passValues;
	}

}
